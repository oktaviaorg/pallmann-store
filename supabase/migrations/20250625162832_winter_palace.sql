## Erreurs à Éviter : Conseils d''Expert

### Erreurs de Préparation

#### Préparation Insuffisante
- **Surface mal nettoyée** : adhérence défaillante de l''huile
- **Humidité résiduelle** : problèmes de pénétration
- **Finition antérieure non éliminée** : résultat hétérogène
- **Réparations négligées** : défauts visibles après huilage

#### Mauvais Choix de Produit
- **Huile inadaptée** à l''essence de bois
- **Qualité insuffisante** : durabilité réduite
- **Mélange de produits** incompatibles
- **Dilution excessive** : protection insuffisante

### Erreurs d''Application

#### Technique Défaillante
- **Application trop épaisse** : séchage difficile et aspect terne
- **Essuyage insuffisant** : formation de coulures
- **Travail par temps inadéquat** : humidité ou température inadaptée
- **Précipitation entre couches** : adhérence compromise

#### Négligence du Séchage
- **Remise en service prématurée** : marquage et usure
- **Ventilation insuffisante** : séchage incomplet
- **Exposition aux poussières** : surface rugueuse
- **Variations de température** : déformations du bois

### Erreurs d''Entretien

#### Produits Inadaptés
- **Nettoyants agressifs** : dégradation de l''huile
- **Excès d''eau** : gonflement et déformation du bois
- **Cires incompatibles** : encrassement de la surface
- **Négligence de l''entretien** : usure prématurée

## Cas Particuliers et Situations Spécifiques

### Parquets Anciens Précieux

#### Parquets Marquetés
- **Essais préalables** sur zone discrète
- **Application au pinceau fin** pour respecter les motifs
- **Attention aux essences différentes** : absorption variable
- **Protection renforcée** des incrustations délicates

#### Parquets Historiques
- **Consultation d''experts** en patrimoine si nécessaire
- **Respect des techniques traditionnelles** d''époque
- **Documentation photographique** avant intervention
- **Utilisation d''huiles naturelles** exclusivement

### Zones Spécifiques

#### Cuisines et Salles d''Eau
- **Huiles hydrofuges** renforcées
- **Application de couches supplémentaires** aux zones critiques
- **Entretien plus fréquent** (tous les 3-4 mois)
- **Surveillance accrue** de l''étanchéité

#### Escaliers et Passages Intensifs
- **Huiles haute résistance** à l''abrasion
- **Nombre de couches augmenté** (3-4 couches)
- **Entretien préventif** plus fréquent
- **Rénovation partielle** ciblée sur les zones d''usure

