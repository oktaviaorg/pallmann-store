| Moyen | Partiellement dégradé | Ponçage contrôlé et traitement spécifique |
| Dégradé | Friable ou suintant | Dépose et repose ou encapsulation |

## Techniques de ponçage adaptées

### Préparation spécifique

La préparation revêt une importance capitale :

1. **Nettoyage approfondi** pour éliminer les cires et vernis anciens
2. **Fixation des lames instables** par clouage ou vissage discret
3. **Traitement préventif** des zones où le goudron pourrait migrer
4. **Protection renforcée** des zones adjacentes contre la poussière noire

### Ponçage progressif et contrôlé

Le ponçage d''un parquet sur goudron nécessite une approche différente :

#### Première passe : Dégrossissage prudent
- Utilisation d''un **grain plus grossier** (40-60) que d''habitude
- **Vitesse réduite** de la ponceuse à bande
- **Contrôle constant** de la température (le goudron peut ramollir avec la chaleur)
- **Aspiration haute performance** indispensable

#### Passes intermédiaires : Vigilance accrue
- Progression par **petites surfaces** pour surveiller toute remontée de goudron
- Nettoyage des abrasifs **plus fréquent** qu''habituellement
- Utilisation de **produits neutralisants** entre les passes si nécessaire

#### Finition : Précision et minutie
- Ponçage final avec un **grain fin adapté** (100-120 maximum)
- **Égrenage manuel** dans les zones sensibles
- **Dépoussiérage ultra-soigneux** avant application des finitions

## Traitement des problèmes spécifiques

### Remontées de goudron

Si le goudron commence à migrer pendant le ponçage :

1. **Arrêt immédiat** du ponçage dans la zone concernée
2. **Application d''un produit fixateur** spécifique
3. **Refroidissement** de la surface si nécessaire
4. **Reprise du ponçage** après séchage complet

### Odeurs persistantes

Pour neutraliser les odeurs caractéristiques du goudron :

- Application d''un **primaire bloquant** spécialement formulé
- Utilisation de **résines époxy** en fine couche d''isolation
- Traitement avec des **produits désodorisants** spécifiques

## Choix des finitions adaptées

### Primaires d''accrochage spécifiques

Avant toute finition, l''application d''un primaire adapté est essentielle :

- **Primaires à base de résine** pour isoler le support
- **Sous-couches spéciales bois anciens** avec propriétés bloquantes
- **Temps de séchage prolongé** pour une polymérisation complète

### Finitions recommandées

Toutes les finitions ne sont pas compatibles avec un parquet sur goudron :

#### Vitrificateurs bi-composants
- **Excellente barrière** contre les remontées
- **Adhérence optimale** même sur supports difficiles
- **Durabilité supérieure** face aux contraintes spécifiques

#### Huiles durcissantes
- **Pénétration contrôlée** sans réactiver le goudron
- **Aspect chaleureux** qui met en valeur le motif à chevrons
- **Entretien facilité** sans ponçage complet lors des rénovations

#### À éviter absolument
- Cires traditionnelles (risque de mélange avec le goudron)
- Vernis en phase aqueuse (adhérence compromise)
- Huiles naturelles pures (risque d''interaction chimique)

