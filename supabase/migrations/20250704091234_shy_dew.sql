## Comprendre la vitrification pour mieux s''en sortir

Avant d''attaquer, faut capter un truc : la vitrification, c''est une sorte de "vernis blindé" qui forme un film de protection sur le bois. Un genre de bouclier en polyuréthane. Ça ne pénètre pas comme l''huile, mais ça protège à mort en surface.

Du coup, pour les rayures, tout dépend si elles restent dans la couche de vitrificateur ou si elles touchent carrément le bois. On vous explique tout juste après.

## Les méthodes pour réparer les rayures sur un parquet vitrifié

### 1. Le stylo correcteur
Idéal pour les griffures toutes fines sur vos parquets à Colmar ou Sarrebourg. Ce petit stylo magique va refondre la pellicule du vitrificateur et faire disparaître la rayure en douceur.

- Nettoyez bien la zone
- Appliquez le stylo dans le sens du bois
- Laissez sécher et frottez doucement

### 2. La cire de réparation
Un poil plus profond que la rayure de surface ? Sortez la cire !

- Choisissez une teinte proche de votre parquet (sapin, chêne, hêtre...)
- Chauffez un peu la cire
- Appliquez-la dans la rayure avec une spatule
- Laissez refroidir et frottez

### 3. Le ponçage localisé
Si ça a vraiment entaillé le bois (genre votre gosse a fait du roller dans le salon à Metz...), on sort les grands moyens :

- Ponçage léger de la zone abîmée (grain fin)
- Enlèvement de la poussière
- Application d''une nouvelle couche de vitrificateur

Attention hein, faut y aller mollo pour pas faire un "trou" trop visible. Si vous êtes pas sûr de vous, appelez-nous !

## Quand faut-il envisager une rénovation complète ?

Parfois, faut arrêter de rafistoler et passer à la vraie réno. Si :

- Y a des rayures partout
- La protection est usée jusqu''à la moelle
- Le parquet brille plus du tout
- Vous voulez changer le style (mat, satiné, ou effet huilé)

Alors go pour un ponçage complet + vitrification. On le fait régulièrement à Strasbourg, Thionville ou encore Dole.

## Prévenir les rayures (plutôt que de pleurer après coup)

Voici les bons réflexes :

- Patins feutre sous les chaises et meubles
- Tapis dans les zones de passage (cuisine, couloir...)
- Pas de talons aiguilles ni crampons sur le parquet
- Un coup de balai microfibre régulier
- Produits d''entretien spéciaux vitrifiés (pas de savon noir hein !)

## Les boulettes à éviter

- Utiliser de l''acétone ou du dissolvant (non mais !)
- Poncer comme un bourrin
- Travailler sur un parquet humide
- Zapper les consignes du fabricant

