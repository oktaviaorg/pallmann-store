## Les bases à retenir avant d''astiquer

Avant de sortir la serpillère, deux-trois trucs à savoir : un parquet ancien ciré, c''est fragile, mais pas capricieux. Faut juste les bons gestes et les bons produits.

### Quels produits utiliser (ou pas)

On va à l''essentiel, en mode Alsace-Lorraine-pratique :

- **Cristaux de soude** : 1 verre dans 2 litres d''eau bien chaude. Ça décrasse sans péter la cire.
- **Eau tiède + liquide vaisselle doux** : Idéal pour l''entretien léger hebdo. Pas besoin d''en faire des caisses.
- **Savon naturel liquide** : Pour ceux qui veulent rester 100% bois et nature.

Et surtout : pas de javel, pas d''acide, pas d''éponge grattante ! Vous tenez à votre parquet, non ?

Un petit plus pour les puristes : l''infusion de savon noir (en toute petite dose) fonctionne aussi très bien pour nourrir le bois tout en nettoyant.

### Fréquence de nettoyage recommandée

Un beau parquet, c''est comme une vieille voiture : ça s''entretient. Sinon ça s''encrasse. Voici notre routine de pro :

- **Tous les jours (ou presque)** : Aspi avec brosse douce ou balai microfibre. On vire poussières et miettes.
- **Une fois par semaine** : Passage humide (très léger, on parle pas de trempage) avec produit doux.
- **Tous les 6 mois / 1 an** : Application d''une fine couche de cire. Et hop, le bois retrouve son petit lustre d''antan.

Petit conseil bonus : alterner entre la cire d''abeille et la cire carnauba selon la saison. La première est plus souple en hiver, la deuxième plus dure pour l''été.

### Méthode de nettoyage sans mouiller à fond

Là où beaucoup se plantent, c''est avec l''eau. Trop d''humidité = bois qui gonfle = parquet qui gondole. Donc on fait simple :

- Éponge ou serpillère bien essorée
- Rinçage rapide
- Et surtout : séchage immédiat avec balai microfibre sec

Astuce maison : aérez bien la pièce après, ça aide !

Pour les surfaces très grandes, travaillez par petites zones et évitez de mouiller plus d''1 m² à la fois.

## Cas concrets : et si... ?

### 1. Une tâche d''eau ou de vin ?

- Tamponnez tout de suite avec chiffon sec
- S''il reste une trace : éponge tiède + savon doux
- Et un petit coup de chiffon sec pour finir

### 2. Le parquet est bien sale, genre après chantier ou locataires peu soigneux ?

- Décrassage avec les fameux cristaux de soude
- Léger rinçage
- Séchage express

### 3. Il a un look terne et poussiéreux même après nettoyage ?

C''est que la cire est fatiguée. Ajoutez une couche fine, lustrée doucement avec une mèche de coton. Le résultat va vous bluffer.

### 4. Le parquet est rayé ?

- Pour les rayures fines, frottez avec un chiffon imbibé d''un mélange cire + essence de térébenthine.
- Pour les plus marquées : un petit ponçage localisé à la laine d''acier 000 puis réapplication de cire.

## Produits à éviter (vraiment, hein)

- Poudre à récurer ? Oubliez.
- Eau de javel ou ammoniac ? Interdits de parquet.
- Acétone ? Vous voulez vraiment voir votre cire fondre ?

Toujours tester un produit sur un coin discret avant de le balancer partout. Vaut mieux 10 secondes de précaution que 10 heures de regrets.

Et n''oubliez pas : même les produits à base d''huiles essentielles peuvent être trop agressifs pour la cire. Prudence.

## Envie de changer d''ambiance ? Le décireur entre en jeu

Des fois, vous avez envie de repartir à zéro. Nouveau look, finition moderne, huilée, mate... Dans ce cas, on passe par la case "décireur".

- **Produits spécialisés** : Dispo dans le commerce, à base de solvants doux
- **Recette maison** : Un peu old school mais efficace : alcool à brûler + térébenthine + huile de lin
- **Cristaux de soude + ammoniaque** (attention, à manier avec précaution !)

Après un bon décireur, prévoyez toujours un ponçage doux et une nouvelle finition, sinon le bois reste à nu et fragile.

Petit rappel : les parquets cirés des maisons anciennes en Alsace sont souvent en sapin rouge ou chêne massif. Chaque essence réagit différemment, donc ça vaut le coup d''avoir un avis pro.

