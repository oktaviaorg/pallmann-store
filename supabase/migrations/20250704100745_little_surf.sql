C''est une question qui revient souvent sur les chantiers de rénovation dans l''Est de la France : "Faut-il reboucher les interstices de mon vieux parquet cloué avec de la pâte à bois ?" La réponse est non, non, et encore non. C''est un réflexe compréhensible, mais qui peut coûter cher à long terme.

Chez Les Ponceurs Réunis, on intervient sur d''anciens planchers à Mulhouse, Nancy, Strasbourg ou encore dans les villages alsaciens, et on voit trop souvent les dégâts causés par ce genre de "bricolage bien intentionné". On vous explique pourquoi cette pratique est à proscrire, et surtout, ce qu''on fait à la place.

## Pourquoi ces interstices apparaissent ?

Avant de parler de solution, petit rappel utile : le bois est un matériau vivant. Avec les variations de température et d''humidité, il gonfle, il rétrécit, il travaille, quoi. Et dans le cas d''un parquet cloué sur lambourdes ou solives, ce mouvement est encore plus libre.

Les lames bougent avec les saisons, surtout sur les parquets posés au 19e ou début 20e si courants à Belfort, Reims, Sélestat ou Thann. Ces petits jours entre les lames, ces fentes irrégulières, c''est tout à fait normal. Et même si c''est pas toujours hyper pratique pour l''aspirateur, ça fait partie de leur charme.

## L''erreur de la pâte à bois : pourquoi ça ne tient pas

La pâte à bois ou les masticages maison (poussière de ponçage + colle) sont tentants pour "faire propre". Mais ça tient rarement plus de 12 mois. Pourquoi ? Parce que ce genre de produit est rigide. Et le bois, lui, continue à bouger. Donc le joint se fissure, s''effrite, parfois il tombe carrément en morceaux.

On a vu des cas, notamment dans des appartements anciens à Metz ou Colmar, où la pâte avait noirci, s''était craquelée, et piégeait la poussière. Résultat : un parquet encore plus moche qu''avant intervention.

### Conséquences :

- Aspect irrégulier et "bricolé"
- Décollement de la finition (huile, vernis...)
- Interstices sales, poreux, impossible à entretenir
- Obligation de tout refaire à court terme (donc double budget)

En clair : on perd du temps, de l''argent, et on abîme le cachet du sol.

## Notre solution : brossage, aspiration, protection

La méthode Les Ponceurs Réunis ? Elle est simple, propre, durable :

- Grattage en douceur des interstices avec brosse fine ou grattoir en acier
- Aspiration minutieuse (même avec embout spécial si besoin)
- Application d''un fond dur, d''une huile naturelle ou d''un vernis mat

Ce traitement permet de saturer les pourtours des lames, empêchant l''humidité ou les saletés de s''y infiltrer.

## Et ça donne quoi ?

Eh bien un parquet ancien propre, authentique, sain. Les interstices restent visibles, oui. Mais propres, nets, et assumés. C''est un look rustique et élégant qui matche parfaitement avec les ambiances anciennes, les moulures, les cheminées d''origine...

D''ailleurs, dans les châteaux où on intervient (par exemple en Moselle ou dans le Sundgau), personne ne rebouche ces fentes. On les brosse, on les nettoie, on les protège. Point barre.

## Et s''il y a trop d''espace entre les lames ?

Alors là, on peut envisager des flipots (petites lames insérées sur mesure), mais uniquement si les fentes sont trop larges (>8 mm). Et ça se fait dans les règles : bois de même essence, collés mais pas bridés, puis finition homogène.

Encore une fois, pas de mastic. Pas de silicone. Pas de boucherie.

## En conclusion : l''authentique, c''est mieux

Si vous avez un parquet ancien cloué avec des interstices, considérez-les comme un trésor, pas un défaut. Et traitez-les comme tel : nettoyage, soin, respect. Pas de remplissage artificiel qui va s''effondrer en deux hivers.

Et surtout : faites appel à des pros qui connaissent ces planchers comme leur poche. Chez Les Ponceurs Réunis, on ne transforme pas vos parquets anciens en imitation moderne. On leur redonne simplement leur éclat d''origine.

