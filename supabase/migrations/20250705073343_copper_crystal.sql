## Rénovation de parquet : Quand et comment intervenir ?

### Quand rénover ?

- Lorsque le parquet présente des rayures profondes ou des taches incrustées
- Si la finition (huile ou vernis) s''est usée au fil du temps

### Comment rénover ?

1. **Ponçage** : Retirez les anciennes couches de finition
2. **Nettoyage** : Aspirez soigneusement la poussière générée
3. **Nouvelle finition** : Appliquez une huile ou un vernis pour protéger le bois

## Réparation de parquet ancien : Ponçage et reprises après démolition

Suite à des travaux comme la dépose de cloisons ou la démolition de murs, des ajustements sont souvent nécessaires pour harmoniser le sol.

### Problèmes fréquents suite à des démolitions :

- Des trous ou manques dans le parquet là où se trouvaient les cloisons
- Des différences de niveau entre les zones adjacentes au mur supprimé
- Une perte d''harmonie dans le motif du parquet

### Solutions adaptées :

- **Insertion de nouvelles lames** : Remplissez les espaces laissés par les cloisons avec des lames compatibles en termes d''essence et de teinte
- **Nivellement** : Si le sol présente des différences de hauteur, réalisez un ponçage ciblé pour harmoniser la surface
- **Reprise du motif** : Pour les parquets en damier ou mosaïque, découpez précisément les nouvelles pièces pour qu''elles s''intègrent parfaitement au dessin existant

## 5 erreurs à éviter lors de l''entretien d''un parquet

1. **Utiliser trop d''eau lors du nettoyage** : cela peut gonfler les lames
2. **Appliquer des produits non adaptés** : préférez des nettoyants spécifiques pour parquets
3. **Négliger les rayures légères** : elles peuvent s''aggraver avec le temps
4. **Ne pas huiler régulièrement un parquet huilé** : cela réduit sa protection
5. **Poncer trop souvent** : cela peut fragiliser les parquets massifs

## Parquet dans une salle de bain : précautions spéciales

L''installation d''un parquet dans une salle de bain peut apporter une touche chaleureuse et élégante, mais nécessite des précautions particulières :

### À éviter absolument :

- **Choisir des essences européennes non traitées** : Privilégiez des essences exotiques naturellement résistantes à l''humidité ou des essences européennes traitées spécifiquement
- **Poser un parquet flottant** : L''eau peut s''infiltrer entre les lames. Optez pour une pose collée qui assure une meilleure étanchéité
- **Opter pour la vitrification standard** : Préférez une finition huilée qui protège le bois sans l''étouffer et permet des réparations locales
- **Négliger l''étanchéité** : Renforcez la protection avec une colle époxy et des joints "pont de bateau" pour permettre les variations dimensionnelles du bois tout en assurant une étanchéité optimale

## Quand remplacer votre parquet ? Les signes qui ne trompent pas

Certaines situations nécessitent un remplacement plutôt qu''une simple rénovation :

1. **Dégâts des eaux importants** : Parquet gondolé ou présentant des signes de moisissure
2. **Usure excessive** : Rayures profondes, taches persistantes ou décoloration importante que le ponçage ne peut corriger
3. **Déformations structurelles** : Zones bombées ou affaissements du parquet
4. **Grincements et instabilité** : Dus à des lambourdes vermoulues ou à une pose défectueuse
5. **Problèmes structurels** : Signes d''affaissement indiquant que la structure sous-jacente est endommagée

