## Le processus de vitrification en 3 étapes

### 1. Ponçage du parquet

Le ponçage est une étape cruciale qui conditionne la qualité finale de la vitrification :

- **Utilisation de machines professionnelles** (ponceuse à bande, bordureuse)
- **3 passes** : gros grain (40-60), moyen (80-100), fin (120-150)
- **Préparation de la surface** de façon homogène
- **Égrenage** entre les couches de vitrificateur

### 2. Nettoyage du sol

Un nettoyage méticuleux est indispensable avant l''application du vitrificateur :

- **Aspiration minutieuse** des poussières
- **Vérification des joints** et des défauts
- **Application d''un fond dur** si besoin
- **Dépoussiérage** avec chiffon microfibre

### 3. Application du vitrificateur

L''application proprement dite requiert précision et méthode :

- **2 à 3 couches fines** au rouleau spécial vitrificateur
- **Séchage entre chaque couche** (3h à 6h selon le produit)
- **Respect des temps de séchage** pour éviter les marques
- **Égrenage léger** entre les couches pour une meilleure adhérence

## Erreurs à éviter

Pour une vitrification réussie, évitez ces erreurs courantes :

- **Utiliser trop de produit** : cela crée des bulles et des coulures
- **Ne pas bien poncer** : le vitrificateur ne tient pas sur une surface mal préparée
- **Choisir un vernis incompatible** avec l''essence de bois
- **Appliquer sur un sol humide** ou non préparé
- **Ne pas prévoir de ventilation** pendant le séchage
- **Négliger l''égrenage** entre les couches

