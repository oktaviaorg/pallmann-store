## Les grains de papier de verre : comment bien choisir ?

Le choix du grain est essentiel pour obtenir une finition parfaite sans endommager la surface.

### Comprendre la classification des grains

Le grain correspond à la taille des particules abrasives sur le papier. Plus le numéro est élevé, plus le grain est fin.

- **Grains grossiers (40-60)** : Idéal pour enlever rapidement des couches épaisses comme la peinture ou le vernis
- **Grains moyens (80-150)** : Parfait pour éliminer les rayures laissées par un ponçage grossier
- **Grains fins (180-320)** : Utilisés pour lisser la surface avant l''application d''une finition

### Choisir le grain selon l''étape du ponçage

| Étape du ponçage | Grain recommandé |
|------------------|------------------|
| Décapage | 40 à 60 |
| Lissage intermédiaire | 80 à 150 |
| Finition | 180 à 320 |

## Foire aux questions sur les tarifs de ponçage

### Combien coûte le ponçage d''un parquet de 20m² ?
Pour une surface de 20m², comptez entre 300€ et 600€ pour un ponçage seul, et entre 500€ et 900€ pour un ponçage avec finition, selon l''état du parquet et la région.

### Le ponçage de parquet est-il éligible à la TVA à taux réduit ?
Oui, le ponçage de parquet bénéficie d''une TVA à 10% s''il est réalisé dans une habitation achevée depuis plus de 2 ans.

### Quelle est la durée de vie d''un ponçage de parquet ?
Un parquet poncé et bien entretenu peut conserver son aspect pendant 8 à 15 ans avant de nécessiter un nouveau ponçage, selon l''intensité de son utilisation.

### Peut-on poncer tous les types de parquets ?
Non, seuls les parquets massifs et certains contrecollés (avec une couche d''usure suffisante) peuvent être poncés. Les stratifiés ne peuvent pas être poncés.

### Combien de fois peut-on poncer un parquet massif ?
Un parquet massif peut généralement être poncé 4 à 8 fois au cours de sa vie, selon son épaisseur initiale et la profondeur de chaque ponçage.

