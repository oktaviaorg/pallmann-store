### 2. Polish spécifique pour parquet vitrifié
- Idéal pour les petites zones à traiter
- Application au chiffon doux
- Effet immédiat sur les micro-rayures
- Nécessite un renouvellement régulier

### 3. Mélange huile de lin et alcool à brûler
- Solution traditionnelle et économique
- Proportions : 1 part d''huile de lin pour 3 parts d''alcool à brûler
- Appliquer en fine couche avec un chiffon doux
- Efficace pour redonner de l''éclat aux zones ternies

### 4. Crayons de retouche
- Parfaits pour les petites rayures localisées
- Disponibles en différentes teintes pour s''adapter à votre parquet
- Application précise et discrète
- Solution temporaire mais efficace

