## Comment entretenir un parquet vitrifié après réparation ?

Pour préserver la beauté de votre parquet vitrifié fraîchement réparé :

### Entretien quotidien
- Utilisez un **balai microfibre** ou un **aspirateur avec brosse parquet** pour éliminer poussières et saletés
- Évitez l''eau stagnante qui peut s''infiltrer entre les lames
- Essuyez immédiatement tout liquide renversé

### Nettoyage hebdomadaire
- Passez une **serpillère très légèrement humide** (quasi sèche)
- Utilisez un **nettoyant spécifique pour parquet vitrifié** (pH neutre)
- Évitez les produits ammoniaqués ou trop agressifs qui ternissent la vitrification

### Entretien préventif
- Placez des **patins sous les pieds des meubles**
- Utilisez des **tapis aux entrées** pour limiter les gravillons et saletés abrasives
- Évitez les **talons aiguilles** et chaussures à crampons sur le parquet

### Entretien périodique (tous les 6 mois)
- Appliquez un **rénovateur métallisant** pour raviver l''éclat
- Vérifiez l''apparition éventuelle de nouvelles rayures
- Traitez rapidement les petits dommages avant qu''ils ne s''aggravent

