## FAQ : Comment rénover soi-même un parquet ancien ?

### 1. Quelles sont les étapes clés pour rénover un parquet ancien ?

La rénovation d''un parquet ancien suit un processus précis :

**Préparation** : Retirer les clous saillants et combler les trous avec de la pâte à bois.

**Remplacement des lames abîmées** : Découper et coller de nouvelles lames compatibles.

**Ponçage** : Utiliser une ponceuse à bande (grain 40 puis 120) en suivant le sens du bois.

**Réparation des fissures** : Combler avec un mélange sciure/colle ou résine époxy.

**Application de finition** : Choix entre vitrificateur (résistance), huile (aspect naturel) ou cire (patine traditionnelle).

### 2. Quel matériel est indispensable pour ce projet ?

- Ponceuse à parquet + abrasifs (grains 40, 80, 120)
- Bordureuse pour les angles
- Mastic à bois et spatule
- Produits de finition (vitrificateur polyuréthane, huile durcissante)
- Protections : Masque FFP3, lunettes, casque antibruit 

### 3. Comment réparer les écartements entre les lames sans professionnel ?

**Petites fissures (<3 mm)** :
- Mélanger sciure fine et colle à bois pour créer un enduit teinté.

**Écartements importants** :
- Insérer des lamelles de bois collées
- Utiliser de la corde de jute imprégnée d''huile de lin pour un effet rustique

### 4. Quelle finition choisir selon l''usage de la pièce ?

| Finition | Avantages | Inconvénients | Usage recommandé |
|----------|-----------|---------------|------------------|
| Vitrification | Résiste à l''eau, facile à nettoyer | Masque le grain du bois | Pièces humides (cuisine) |
| Huilage | Met en valeur les veinures, réparable localement | Entretien annuel | Salons, chambres |
| Cirage | Patine profonde, authentique | Sensible aux taches | Pièces peu fréquentées |

### 5. Est-il possible de rénover sans ponçage ?

Oui, pour les parquets légèrement usés :

**Nettoyage profond** : Utiliser un dégraissant spécifique (pH neutre)

**Screen & recoat** : Poncer légèrement avec une monobrosse + abrasif fin (120 grit) avant une nouvelle couche de vitrificateur.

**Rénovateur bois** : Appliquer un produit régénérant type Revive pour raviver les couleurs.

### 6. Comment reproduire la teinte d''origine sur des zones réparées ?

**Analyse du bois** : Identifier l''essence (chêne, châtaignier, etc.) et l''âge du parquet.

**Teinture sur mesure** : Mélanger des pigments naturels (terre de Sienne, noir de vigne) avec de l''huile.

**Patine vieillie** : Appliquer une lasure foncée puis estomper avec une éponge humide.

### 7. Erreurs à éviter absolument

**Poncer à contre-sens** : Risque de rayures irréversibles.

**Oublier le dépoussiérage** : Résidus = bulles dans la finition.

**Surcharger en produit** : Épaisseurs excessives = fissuration.

**Astuce pro** : Testez toujours les finitions sur une zone discrète avant application complète.

