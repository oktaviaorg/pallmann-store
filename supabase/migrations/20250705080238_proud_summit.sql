- Recirer une à deux fois par an
- Éviter l''eau et les produits détergents

La cire forme une couche protectrice qui s''use avec le temps. Un entretien régulier permet de maintenir cette protection et de raviver l''éclat du parquet.

### Pour un parquet vitrifié ou avec vitrificateur

- Nettoyage régulier avec un aspirateur ou un balai microfibre
- Utilisation d''un nettoyant spécifique pour parquet vitrifié une fois par semaine
- Éviter l''eau stagnante et les produits abrasifs
- Application d''un rénovateur tous les 2-3 ans pour raviver l''éclat

